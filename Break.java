//java project that uses break.
public class Break { // creates and opens a class named Break.
    public static void main(String[] args) {  //Mandatory statement in java programs. Opens a method.
        for(int i=1;i<=10;i++)
        { //opens for loop.
            if(i==5){
                break; //breaks the for loop when i is equal to 5.
            }
            System.out.println(i);//prints the values of i.
        }//closes for loop.
    }//closes method.

    
} //closes a class.
 