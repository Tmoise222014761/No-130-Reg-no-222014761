//Java project that uses do while to print numbers from to 10
public class Dowhileloop { //declares and the main class named Dowhileloop
    public static void main(String[] args) { // opens a main method.
        int i=1; // initialisation of variable i with value 1 aand datatype integer.
        do{
            System.out.println(i); //prints the values of i.
            i++; //incrementarion of values of i.
        }
        while(i<=10); // while loop to test condition after executing statements.

    } //closes the main method.
    
} // closes the main class.
