//Java programs that prints that counts from 1 to 12.
public class Whileloop { //opens a class named While loop.
    public static void main(String[] args) { //opens a method.
        int a=1; //initialisation of variable a to 1.
        while(a<=12){  //creates and opens a while loop.
            System.out.println(a); // prints values of a.
            a++;  //increments variable a.    

     } //closes a while loop.

    } //closes a method.
    
}//closes a class.
